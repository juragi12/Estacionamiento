package co.com.ceiba.estacionamiento.juan.giraldo.aplicacion.helper;

public class TiempoEstadia {

	private int dias;
	private int horas;

	public TiempoEstadia(int dias, int horas) {
		this.dias = dias;
		this.horas = horas;
	}

	public int getDias() {
		return dias;
	}

	public int getHoras() {
		return horas;
	}

}
